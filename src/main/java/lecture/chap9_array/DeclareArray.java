class DeclareArray 
{
    public static void main(String[] args)
    {
        // define array
        String[] studentName;
        
        // create array
        studentName = new String[3];
        
        // initialise each array element
        studentName[0] = "Hermione";
        studentName[1] = "Harry";
        studentName[2] = "Ron";
    }
}
